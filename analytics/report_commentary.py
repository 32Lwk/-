from __future__ import annotations

from typing import Any, Dict


def _fmt(x: float, nd: int = 4) -> str:
    return f"{x:.{nd}f}"


def exec_summary_commentary(ctx: Dict[str, str]) -> str:
    return (
        f"**考察（要約）**: 本データは **n = {ctx['n_rows']}** 件、**全体CVR = {ctx['cvr']}** である。mid_cost シナリオで最頻の推奨が「{ctx['top_treat']}」（{ctx['top_share']}）となるのは、"
        "期待利益（購入確率の予測 × `history` − コスト仮定）のもとで、**その処置が多数の顧客に対して支配的だった**ことを意味する。"
        "条件付きCVRが高い処置（例: 割引）でも、コスト項が大きければ期待利益では劣後しうる。**採用ターゲティングモデル**は AUC 最大の `{ctx['best_model']}`（logreg AUC {ctx['auc_lr']}、HGB AUC {ctx['auc_hgb']}）であり、"
        "ランキングは有用でも **個別処置効果の点推定精度**や **未観測交絡**は別問題である。経営判断では **シナリオ感度**・**OOF/ホールドアウト**・**ESS** をセットで見ることを推奨する。\n\n"
    )


def commentary_context(
    qc: Dict[str, Any],
    model_eval: Dict[str, Dict[str, float]],
    best_model: str,
    policy_mid_summary: Dict[str, object],
) -> Dict[str, str]:
    top_t = str(policy_mid_summary.get("top_treatment", "")).replace(" | ", " / ")
    top_s = float(policy_mid_summary.get("top_share", 0.0))
    cr = float(qc.get("conversion_rate", 0.0))
    n = int(qc.get("n_rows", 0))
    lr = model_eval.get("logreg", {})
    hgb = model_eval.get("hgb", {})
    return {
        "n_rows": str(n),
        "cvr": _fmt(cr),
        "best_model": best_model,
        "auc_lr": _fmt(float(lr.get("auc", 0))),
        "auc_hgb": _fmt(float(hgb.get("auc", 0))),
        "ap_lr": _fmt(float(lr.get("average_precision", 0))),
        "top_treat": top_t,
        "top_share": f"{top_s:.1%}",
    }


